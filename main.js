function isVersionNewer(a, b) {
  if (!a)
    return false;
  if (!b)
    return true;
  const pa = a.split(".").map((n) => parseInt(n, 10));
  const pb = b.split(".").map((n) => parseInt(n, 10));
  const len = Math.max(pa.length, pb.length);
  for (let i = 0; i < len; i++) {
    const av = pa[i] || 0;
    const bv = pb[i] || 0;
    if (av > bv)
      return true;
    if (av < bv)
      return false;
  }
  return false;
}

